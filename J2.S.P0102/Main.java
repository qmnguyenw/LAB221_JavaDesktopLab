    private void displayTime() {
        time = new Thread(new Runnable() {
            @Override
            public void run() {
                while (true) {
                    Calendar calendar = Calendar.getInstance();
                    //YYYY-MM-dd hh:mm:ss
                    SimpleDateFormat sdf = new SimpleDateFormat("hh:mm:ss");
                    jlbTime.setText(sdf.format(calendar.getTime()));
                    try {
                        time.sleep(1000);
                    } catch (Exception ex) {
                    }
                }
            }
        });
    }

